package ch02.sec01;

public class VariableInitializationExample {
    public static void main(String[] args) {
        //변수 value 선언
        int value;

        //연산 결과를 변수 result의 초기값으로 대입
        int result = value + 10; //컴파일 에러 발생
        //위 연산에서 변수가 선언되었지만 초기화되지 않았기 때문에 컴파일 에러 발생

        System.out.println(result);
    }
}
